#!/usr/bin/env node
// Reads skills/*.md and emits src/lib/skill-metadata.ts — a client-safe
// snapshot of skill metadata (no systemPrompt). Runs automatically before
// `npm run dev` and `npm run build` so dropping in a new skills/*.md file
// is all that's needed to register a skill.
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import matter from 'gray-matter';

const here = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(here, '..');
const skillsDir = path.join(root, 'skills');
const outFile = path.join(root, 'src/lib/skill-metadata.ts');

const ORDER = [
  'expense-categorizer',
  'vendor-risk-flagger',
  'invoice-extractor',
  'policy-compliance-checker',
  'spend-anomaly-detector',
  'meeting-cost-calculator',
];

const files = fs
  .readdirSync(skillsDir)
  .filter((f) => f.endsWith('.md'))
  .sort();

const metadata = files.map((file) => {
  const raw = fs.readFileSync(path.join(skillsDir, file), 'utf-8');
  const { data } = matter(raw);
  return {
    id: String(data.id),
    name: String(data.name),
    description: String(data.description),
    icon: String(data.icon),
    color: String(data.color),
    category: String(data.category),
    exampleInputs: Array.isArray(data.exampleInputs)
      ? data.exampleInputs
      : [],
    outputFormat: String(data.outputFormat ?? ''),
    chainableAfter: Array.isArray(data.chainableAfter)
      ? data.chainableAfter
      : undefined,
  };
});

metadata.sort((a, b) => {
  const ai = ORDER.indexOf(a.id);
  const bi = ORDER.indexOf(b.id);
  return (ai === -1 ? 999 : ai) - (bi === -1 ? 999 : bi);
});

const body = `// AUTO-GENERATED FROM skills/*.md — DO NOT EDIT BY HAND.
// Regenerated by scripts/build-skill-metadata.mjs (runs via predev / prebuild).
import type { SkillMeta } from './types';

export const skillMetadata: SkillMeta[] = ${JSON.stringify(metadata, null, 2)};

export function getSkillMetaById(id: string): SkillMeta | undefined {
  return skillMetadata.find((s) => s.id === id);
}
`;

fs.writeFileSync(outFile, body);
console.log(`Wrote ${metadata.length} skills to src/lib/skill-metadata.ts`);
